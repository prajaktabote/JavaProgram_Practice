public class IfElseLadder
{
    public static void main(String args[])
    {
        int a=50;
        if(a==30)
        System.out.println("a is 30");
        else if(a==50)
        System.out.println("a is 50");
        else if(a==20)
        System.out.println("a is 20");
        else
        System.out.println("a is not present");
    }
}