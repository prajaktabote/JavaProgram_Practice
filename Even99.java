import java.util.*;
class Even99{
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter a number");
        int n=sc.nextInt();
       
        if(n%2==0){
            System.out.println("Even");
        }
        else{
            System.out.println("odd number");
        
        }
        
    }
}