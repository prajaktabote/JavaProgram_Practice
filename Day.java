//Write a program to print countdown of days to submit assignment

class Day{
    public static void main(String args[]){
        int n=0;
        if(n!=0){
            while(n>0){
                System.out.println(n+ " day remaining");
                n--;
            }
            }else{
                System.out.println(n+"days assignment is overdue");
            }
        
    }
}